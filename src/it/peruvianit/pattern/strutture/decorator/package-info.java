/**
 * <h1>Package it.peruvianit.pattern.strutture.decorator</h1>
 * Il pattern Decorator permette di aggiungere in modo dinamico nuove funzionalità a un oggetto, senza modificarne
 * la struttura. Invece di usare l'ereditarietà, che creerebbe una gerarchia rigida di classi, questo pattern "avvolge"
 * l'oggetto di base con un altro oggetto chiamato decoratore. Ogni decoratore aggiunge un comportamento specifico, come
 * un costo extra o una nuova proprietà. Pensa a un semplice caffè: puoi "decorarlo" aggiungendo latte o zucchero, dove
 * ogni aggiunta è un decoratore che estende le funzionalità del caffè di base. Questo metodo offre grande flessibilità,
 * consentendo di combinare diverse funzionalità a piacere.
 *
 * @since 1.0
 * @author Sergio Arellano
 */
